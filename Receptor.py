
class Receptor:
    
    def __init__(self, cadena):
        self.cadena = cadena
        

    # capa de transmision
    def recibir_objeto(self):
        self.cadena

    # capa de codificacion
    def codificacion(self):
        self.cadena

    # capa de verificacion
    def recibir_cadena_segura(self):
        self.cadena

    # capa de aplicacion
    def recibir_cadena(self):
        self.cadena

